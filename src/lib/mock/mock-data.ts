const generateComments = (postId: string, count: number): Comment[] => {
  const commentTexts = [
    'This pattern is absolutely stunning! Love the color choices.',
    'Where did you source these fabrics? I need to try this!',
    'Great quilting! The stitching is perfect.',
    'This made my day better. Thank you for sharing your work!',
    'I\'ve been following your quilts for a while, this is one of your best!',
    'The colors in this are incredible. Beautiful fabric selection.',
    'Can\'t wait to see the finished quilt!',
    'This speaks to my soul. Beautiful craftsmanship.',
    'Wow! Just wow. Keep quilting!',
    'Perfection in every stitch.',
  ];

  const shuffled = [...commentTexts].sort(() => Math.random() - 0.5);
  return Array.from({ length: count }, (_, i) => ({
    id: `${postId}-comment-${i}`,
    userId: users[Math.floor(Math.random() * users.length)].id,
    user: users[Math.floor(Math.random() * users.length)],
    content: shuffled[i % shuffled.length],
    createdAt: new Date(Date.now() - Math.random() * 86400000 * 2).toISOString(),
    likes: Math.floor(Math.random() * 50),
  }));
};

